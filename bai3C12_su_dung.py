from bai3C12_co_ban import cong, tru
from bai3C12_nang_cao import luy_thua, can_bac_hai

ket_qua_cong = cong(10, 5)
ket_qua_tru = tru(10, 5)
ket_qua_luy_thua = luy_thua(2, 3)
ket_qua_can_bac_hai = can_bac_hai(16)

print(f"10 + 5 = {ket_qua_cong}")
print(f"10 - 5 = {ket_qua_tru}")
print(f"2 ^ 3 = {ket_qua_luy_thua}")
print(f"Căn bậc hai của 16 = {ket_qua_can_bac_hai}")